/*
TODO: Problem-18: Use a for loop to find the sum of the numbers from 25 to 75 and print the sum at the end.
*/

// Solution:
let sum = 0; // Initialize the sum variable to 0
for(let i = 25; i <= 75; i++){
    sum += i; // Add the current number to the sum
}
console.log(`The sum of numbers from 25 to 75 is: ${sum}`);
// Output:  
// The sum of numbers from 25 to 75 is: 3825
// Explanation:
// The sum of numbers from 25 to 75 is calculated by adding all the numbers in that range.