// Solution:

const friends = ['Raju', 'Rana', 'Hasan', 'Kabir', 'Mahi'];
const friendsString = friends.join(','); // Join the array elements into a string separated by commas
console.log(friendsString); // Output: 'Raju,Rana,Hasan,Kabir,Mahi'